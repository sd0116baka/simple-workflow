import { FIXTURE_TRACE_OPERATION } from "./state-fixture-trace-operations.js";

const {
  EXECUTION_INTENT,
  EXECUTION_AUTHORIZATION,
  ISOLATED_WORKSPACE,
  MAIN_AGENT_INITIALIZATION,
  EXECUTION_REPORT,
  REVIEW_REPORT,
  CONVERGENCE_ADVICE,
  CONVERGENCE_SUCCESS,
  CONVERGENCE_SUCCESS_DECISION_REQUEST,
  ACCEPT_CONVERGENCE_DECISION,
  AUTO_MERGE_PLAN,
  AUTO_MERGE_RESULT,
  MERGED_TASK_CLOSEOUT,
  CANCELLED_CONVERGENCE_FAILURE,
  CANCEL_TASK_DECISION_REQUEST,
  CANCEL_TASK_DECISION,
  CANCELLED_TASK_CLOSEOUT,
} = FIXTURE_TRACE_OPERATION;

export const MAIN_AGENT_READY_TRACE = Object.freeze([
  EXECUTION_INTENT,
  EXECUTION_AUTHORIZATION,
  ISOLATED_WORKSPACE,
  MAIN_AGENT_INITIALIZATION,
]);

export const FIRST_EXECUTION_TRACE = Object.freeze([
  ...MAIN_AGENT_READY_TRACE,
  EXECUTION_REPORT,
]);

export const FIRST_REVIEW_TRACE = Object.freeze([
  ...FIRST_EXECUTION_TRACE,
  REVIEW_REPORT,
]);

export const CONVERGENCE_SUCCESS_DECISION_TRACE = Object.freeze([
  ...FIRST_REVIEW_TRACE,
  CONVERGENCE_ADVICE,
  CONVERGENCE_SUCCESS,
  CONVERGENCE_SUCCESS_DECISION_REQUEST,
]);

const ACCEPTED_CONVERGENCE_TRACE = Object.freeze([
  ...CONVERGENCE_SUCCESS_DECISION_TRACE,
  ACCEPT_CONVERGENCE_DECISION,
]);

const AUTO_MERGE_PLANNED_TRACE = Object.freeze([
  ...ACCEPTED_CONVERGENCE_TRACE,
  AUTO_MERGE_PLAN,
]);

const AUTO_MERGED_TRACE = Object.freeze([
  ...AUTO_MERGE_PLANNED_TRACE,
  AUTO_MERGE_RESULT,
]);

const STAGE_TRACE_OPERATIONS = Object.freeze({
  "task-pool": Object.freeze([]),
  "task-recommender": Object.freeze([EXECUTION_INTENT]),
  "execution-admission": Object.freeze([EXECUTION_INTENT, EXECUTION_AUTHORIZATION]),
  "isolated-workspace": Object.freeze([
    EXECUTION_INTENT,
    EXECUTION_AUTHORIZATION,
    ISOLATED_WORKSPACE,
  ]),
  "main-agent": MAIN_AGENT_READY_TRACE,
  "execution-agent": FIRST_EXECUTION_TRACE,
  "review-agent": FIRST_REVIEW_TRACE,
  convergence: Object.freeze([
    ...FIRST_REVIEW_TRACE,
    CONVERGENCE_ADVICE,
  ]),
  "human-decision": CONVERGENCE_SUCCESS_DECISION_TRACE,
  "auto-merge-planning": ACCEPTED_CONVERGENCE_TRACE,
  "auto-merge-execution": AUTO_MERGE_PLANNED_TRACE,
  merged: AUTO_MERGED_TRACE,
  closed: Object.freeze([
    ...AUTO_MERGED_TRACE,
    MERGED_TASK_CLOSEOUT,
  ]),
  cancelled: Object.freeze([
    CANCELLED_CONVERGENCE_FAILURE,
    CANCEL_TASK_DECISION_REQUEST,
    CANCEL_TASK_DECISION,
    CANCELLED_TASK_CLOSEOUT,
  ]),
});

export function operationsForWorkflowStage(currentWorkStage) {
  return STAGE_TRACE_OPERATIONS[currentWorkStage] ?? [];
}
